-- Скрипт привязки существующих данных к пользователю
-- ============================================================================
-- 
-- ИНСТРУКЦИЯ ПО ПРИМЕНЕНИЮ:
-- 
-- 1. Примените миграции v1.1.0 и v1.1.1 (Liquibase сделает это автоматически)
-- 2. Создайте первого пользователя через API регистрации
-- 3. Узнайте его ID: SELECT id, email FROM users;
-- 4. Замените :USER_ID на актуальный ID в этом скрипте
-- 5. Выполните скрипт вручную через psql или другую утилиту
-- 6. Примените миграцию v1.1.2 для добавления NOT NULL и FK
--
-- ============================================================================

-- Замените :USER_ID на ID первого пользователя (обычно 1)
-- Например: UPDATE categories SET user_id = 1 WHERE user_id IS NULL;

-- Привязываем все категории без user_id к указанному пользователю
UPDATE categories 
SET user_id = :USER_ID 
WHERE user_id IS NULL;

-- Привязываем все транзакции без user_id к указанному пользователю
UPDATE transactions 
SET user_id = :USER_ID 
WHERE user_id IS NULL;

